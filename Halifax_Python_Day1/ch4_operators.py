# Variables for demonstration
a = 10
b = 3

# Addition
print("Addition:", a + b)  # Output: 13

# Subtraction
print("Subtraction:", a - b)  # Output: 7

# Multiplication
print("Multiplication:", a * b)  # Output: 30

# Division
print("Division:", a / b)  # Output: 3.333...

# Floor Division
print("Floor Division:", a // b)  # Output: 3

# Modulus
print("Modulus:", a % b)  # Output: 1

# Exponentiation
print("Exponentiation:", a ** b)  # Output: 1000


# Simple assignment
x = 10
print("x =", x)

# Add AND
x += 3  # Equivalent to x = x + 3
print("x after x += 3:", x)

# Subtract AND
x -= 2  # Equivalent to x = x - 2
print("x after x -= 2:", x)

# Multiply AND
x *= 2  # Equivalent to x = x * 2
print("x after x *= 2:", x)

# Divide AND
x /= 4  # Equivalent to x = x / 4
print("x after x /= 4:", x)

# Modulus AND
x %= 7  # Equivalent to x = x % 7
print("x after x %= 7:", x)

# Exponent AND
x **= 2  # Equivalent to x = x ** 2
print("x after x **= 2:", x)

# Floor Division AND
x //= 2  # Equivalent to x = x // 2
print("x after x //= 2:", x)


# Values for comparison
a = 10
b = 20

# Equal to
print("a == b:", a == b)  # Output: False

# Not equal to
print("a != b:", a != b)  # Output: True

# Greater than
print("a > b:", a > b)  # Output: False

# Less than
print("a < b:", a < b)  # Output: True

# Greater than or equal to
print("a >= 10:", a >= 10)  # Output: True

# Less than or equal to
print("a <= 10:", a <= 10)  # Output: True

# Logical operator examples

# AND operator
print("True and False:", True and False)  # Output: False
print("True and True:", True and True)   # Output: True

# OR operator
print("True or False:", True or False)   # Output: True
print("False or False:", False or False) # Output: False

# NOT operator
print("Not True:", not True)             # Output: False
print("Not False:", not False)           # Output: True

# Combining them in more complex expressions
a = 10
b = 20

# Check if a is greater than 5 and b is greater than 15
print("a > 5 and b > 15:", a > 5 and b > 15)  # Output: True

# Check if a is less than 10 or b is greater than 15
print("a < 10 or b > 15:", a < 10 or b > 15)  # Output: True

# Use not to reverse the condition
print("not (a > 10):", not (a > 10))          # Output: True

