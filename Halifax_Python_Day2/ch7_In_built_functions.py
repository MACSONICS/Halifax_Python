# Using in-built functions in Python

# 1. print(): Display output
print("Hello, Python In-Built Functions!")

# 2. len(): Get the length of a string or list
my_list = [1, 2, 3, 4, 5]
print("Length of the list:", len(my_list))

# 3. type(): Check the data type of a variable
x = 10
print("Data type of x: ", type(x))

# 4. max() and min(): Find the maximum and minimum values
numbers = [23, 56, 12, 89, 45]
print("Maximum value:", max(numbers))
print("Minimum value:", min(numbers))

# 5. sum(): Calculate the sum of elements in a list
print("Sum of numbers:", sum(numbers))

# 6. sorted(): Sort a list in ascending order
print("Sorted list:", sorted(numbers))

# 7. str(), int(), float(): Convert data types
a = 5
print("Convert integer to string:", str(a))
b = "12.5"
print("Convert string to float:", float(b))

# 8. abs(): Get the absolute value of a number
negative_number = -15
print("Absolute value:", abs(negative_number))
